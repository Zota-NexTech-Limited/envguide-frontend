export async function bulkAddMasterData(
  entity: MasterDataEntity,
  items: MasterDataItem[]
): Promise<{ success: boolean; message: string; addedCount?: number }> {
  try {
    // Build payload based on entity type
    const payload = items.map((item) => {
      // sub-fuel-type bulk add uses fuel_type_name instead of ft_id
      if (entity === "sub-fuel-type") {
        return {
          fuel_type_name: item.fuel_type_name || "",
          name: item.name,
        };
      }
      // energy-type bulk add uses energy_source_name instead of es_id
      if (entity === "energy-type") {
        return {
          energy_source_name: item.energy_source_name || "",
          name: item.name,
        };
      }
      // material-composition-metal-type bulk add uses composition_metal_name instead of mcm_id
      if (entity === "material-composition-metal-type") {
        return {
          composition_metal_name: item.composition_metal_name || "",
          name: item.name,
        };
      }
      // country-iso-two, country-iso-three, time-zone bulk add
      if (
        entity === "country-iso-two" ||
        entity === "country-iso-three" ||
        entity === "time-zone"
      ) {
        return {
          country_name: item.country_name || "",
          name: item.name,
        };
      }
      return { name: item.name };
    });

    const res = await fetch(endpoint(entity, "bulk/add"), {
      method: "POST",
      headers: getAuthHeaders(),
      body: JSON.stringify(payload),
    });
    const data = await res.json();
    if (data?.success || data?.status) {
      return {
        success: true,
        message: data?.message || "Bulk import successful",
        addedCount: data?.data?.addedCount || items.length,
      };
    }
    return {
      success: false,
      message: data?.message || "Bulk import failed",
    };
  } catch (error) {
    return {
      success: false,
      message: "An error occurred during bulk import",
    };
  }
}

export async function getDropdownList(
  entity: MasterDataEntity
): Promise<{ id: string; name: string }[]> {
  try {
    const res = await fetch(endpoint(entity, "drop-down-list"), {
      method: "GET",
      headers: getAuthHeaders(),
    });
    const data = await res.json();
    if (Array.isArray(data?.data)) {
      return (data.data as any[]).map((item) => ({
        id: extractId(entity, item),
        name: item.name || "",
      }));
    }
    return [];
  } catch (error) {
    return [];
  }
}

