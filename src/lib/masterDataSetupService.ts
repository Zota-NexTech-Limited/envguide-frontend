function getAuthHeaders() {
  const token = authService.getToken();
  return {
    "Content-Type": "application/json",
    Authorization: token || "",
  } as Record<string, string>;
}

function endpoint(
  entity: MasterDataEntity,
  action: "list/search" | "add" | "update" | "delete" | "bulk/add" | "drop-down-list"
) {
  return `${API_BASE_URL}/api/master-data-setup/${entity}/${action}`;
}

// Get the ID field name for an entity
function getIdField(entity: MasterDataEntity): string {
  return idFieldMapping[entity] || "id";
}

// Extract ID from an item based on entity type
function extractId(entity: MasterDataEntity, item: any): string {
  const idField = getIdField(entity);
  return item[idField]?.toString() || item.id?.toString() || item._id?.toString() || "";
}

export async function listMasterData(
  entity: MasterDataEntity,
  params?: { searchValue?: string }
): Promise<MasterDataItem[]> {
  const url = new URL(endpoint(entity, "list/search"));
  if (params?.searchValue)
    url.searchParams.set("searchValue", params.searchValue);
  const res = await fetch(url.toString(), {
    method: "GET",
    headers: getAuthHeaders(),
  });
  const data = await res.json();
  // Expected: { status, message, code, data: { totalCount, list: [] } }
  const normalizeItem = (item: any): MasterDataItem => {
    const normalized: MasterDataItem = {
      id: extractId(entity, item),
      code: item.code || "",
      name: item.name || "",
      country_name: item.country_name || "",
    };
    // Include additional fields for sub-fuel-type
    if (entity === "sub-fuel-type") {
      normalized.ft_id = item.ft_id || "";
      normalized.fuel_type_name = item.fuel_type_name || "";
    }
    // Include additional fields for energy-type
    if (entity === "energy-type") {
      normalized.es_id = item.es_id || "";
      normalized.energy_source_name = item.energy_source_name || "";
    }
    // Include additional fields for material-composition-metal-type
    if (entity === "material-composition-metal-type") {
      normalized.mcm_id = item.mcm_id || "";
      normalized.composition_metal_name = item.mcm_name || ""; // API returns mcm_name
      normalized.description = item.description || "";
    }
    return normalized;
  };

  if (Array.isArray((data as any)?.data?.list)) {
    return ((data as any).data.list as any[]).map(normalizeItem);
  }
  // Fallbacks
  if (Array.isArray(data)) return (data as any[]).map(normalizeItem);
  if (Array.isArray((data as any)?.data))
    return ((data as any).data as any[]).map(normalizeItem);
  if (Array.isArray((data as any)?.items))
    return ((data as any).items as any[]).map(normalizeItem);
  return [];
}

export async function addMasterData(
  entity: MasterDataEntity,
  item: MasterDataItem
): Promise<{ success: boolean; message?: string }> {
  try {
    // Build payload based on entity type
    const payload: Record<string, any> = { name: item.name };

    // country_name required for geography and time entities
    if (
      (entity === "country-iso-two" ||
        entity === "country-iso-three" ||
        entity === "time-zone") &&
      item.country_name
    ) {
      payload.country_name = item.country_name;
    }

    // sub-fuel-type requires ft_id
    if (entity === "sub-fuel-type" && item.ft_id) {
      payload.ft_id = item.ft_id;
    }
    // energy-type requires es_id
    if (entity === "energy-type" && item.es_id) {
      payload.es_id = item.es_id;
    }
    // material-composition-metal-type requires mcm_id and description (code is auto-generated by backend)
    if (entity === "material-composition-metal-type") {
      if (item.mcm_id) payload.mcm_id = item.mcm_id;
      if (item.description !== undefined) payload.description = item.description;
    }

    const res = await fetch(endpoint(entity, "add"), {
      method: "POST",
      headers: getAuthHeaders(),
      body: JSON.stringify(payload),
    });
    const data = await res.json();
    const success = !!(data?.success ?? data?.status);
    return {
      success,
      message: data?.message,
    };
  } catch (error) {
    return {
      success: false,
      message: "An error occurred while adding item",
    };
  }
}

export async function updateMasterData(
  entity: MasterDataEntity,
  item: MasterDataItem & { id: string }
): Promise<{ success: boolean; message?: string }> {
  try {
    const idField = getIdField(entity);
    const updatePayload: Record<string, any> = {
      [idField]: item.id,
      name: item.name,
    };

    // country_name required for geography and time entities in update
    if (
      (entity === "country-iso-two" ||
        entity === "country-iso-three" ||
        entity === "time-zone") &&
      item.country_name
    ) {
      updatePayload.country_name = item.country_name;
    }

    // sub-fuel-type requires ft_id in update
    if (entity === "sub-fuel-type" && item.ft_id) {
      updatePayload.ft_id = item.ft_id;
    }
    // energy-type requires es_id in update
    if (entity === "energy-type" && item.es_id) {
      updatePayload.es_id = item.es_id;
    }
    // material-composition-metal-type requires mcm_id, description in update (code is auto-generated)
    if (entity === "material-composition-metal-type") {
      if (item.mcm_id) updatePayload.mcm_id = item.mcm_id;
      if (item.description !== undefined) updatePayload.description = item.description;
    }

    const res = await fetch(endpoint(entity, "update"), {
      method: "POST",
      headers: getAuthHeaders(),
      body: JSON.stringify([updatePayload]),
    });
    const data = await res.json();
    const success = !!(data?.success ?? data?.status);
    return {
      success,
      message: data?.message,
    };
  } catch (error) {
    return {
      success: false,
      message: "An error occurred while updating item",
    };
  }
}

